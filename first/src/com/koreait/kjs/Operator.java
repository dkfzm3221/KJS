package com.koreait.kjs;

public class Operator {
	public static void main(String[] args) {
		int n1 = 10;
		n1++; //1값이 올라감
		System.out.println(n1);
		
		++n1;
		System.out.println(n1);
		
		System.out.println("--------------");
		int n2 = 10;
		System.out.println(n2 +1);
		System.out.println(n2);
	}
}
